$(function () {
  $(".as-menu__toggle").on("click", function (e) {
    e.preventDefault();
    var $toggle = $(this);
    var $sub = $toggle.siblings(".as-menu__sub");

    $sub.slideToggle("fast");

    $(".as-menu__sub").not($sub).slideUp("fast");
  });
});